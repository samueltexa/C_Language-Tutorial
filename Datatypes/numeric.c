//integer data type
#include <stdio.h>
int main(){
    printf("The integer is: %.1f\n",2.0);
    printf("The decimal is: %d\n",40);
    printf("The float number is: %.1f\n",30.3);
//conversion specifiers
// %f-- for float and %d-- for decimals.

//Mathematical operations on numeric data types

//Addition
    printf("The sum is: %d\n",3+3);
    printf("The sum is: %f\n",3.1+3.1);

//Multiplication
    printf("The product is: %d\n",3*3);
    printf("The product is: %f\n",3.1*3.1);

//Division
    printf("The quotient is: %d\n",8/2);
    printf("The quotient is: %f\n",8.8/2.2);

//modulus
    printf("The modulus is: %d",3%3);

 return 0;
}